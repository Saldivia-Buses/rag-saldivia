// ArgentineCities is an alias for backward compatibility — callers that
// only need Argentine cities can still use this name.
var ArgentineCities = WorldCities

// LookupCity returns coordinates for a known city (case-insensitive, Unicode-safe).
// Returns nil if not found — caller should fallback to external geocoding API.
func LookupCity(name string) *GeoLocation {
	lower := strings.ToLower(name)
	if loc, ok := WorldCities[lower]; ok {
		return &loc
	}
	// Try without accents (basic normalization)
	normalized := removeAccents(lower)
	if normalized != lower {
		if loc, ok := WorldCities[normalized]; ok {
			return &loc
		}
	}
	return nil
}

// removeAccents replaces common accented characters with ASCII equivalents.
func removeAccents(s string) string {
	replacer := strings.NewReplacer(
		"á", "a", "é", "e", "í", "i", "ó", "o", "ú", "u",
		"ü", "u", "ñ", "n", "ç", "c", "ã", "a", "õ", "o",
		"â", "a", "ê", "e", "î", "i", "ô", "o", "û", "u",
		"à", "a", "è", "e", "ì", "i", "ò", "o", "ù", "u",
		"ä", "a", "ö", "o",
	)
	return replacer.Replace(s)
}
